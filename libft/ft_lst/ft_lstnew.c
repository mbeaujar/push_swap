#include "../inc/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *cell;

	if (!(cell = malloc(sizeof(t_list) * 1)))
		return (NULL);
	cell->content = content;
	cell->next = NULL;
	return (cell);
}
