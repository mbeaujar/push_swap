#include "../inc/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*st;
	unsigned char	cc;
	size_t			i;

	st = (unsigned char*)s;
	cc = (unsigned char)c;
	i = -1;
	while (++i < n)
		if (st[i] == cc)
			return (st + i);
	return (NULL);
}
